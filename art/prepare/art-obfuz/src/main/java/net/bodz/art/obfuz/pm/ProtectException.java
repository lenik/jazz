package net.bodz.art.obfuz.pm;

public class ProtectException
        extends Exception {

    private static final long serialVersionUID = -1484121022530572792L;

    public ProtectException() {
        super();
    }

    public ProtectException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProtectException(String message) {
        super(message);
    }

    public ProtectException(Throwable cause) {
        super(cause);
    }

}
