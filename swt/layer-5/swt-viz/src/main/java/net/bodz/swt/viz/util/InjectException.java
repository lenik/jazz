package net.bodz.swt.viz.util;

public class InjectException
        extends Exception {

    private static final long serialVersionUID = -3941254238064142943L;

    public InjectException() {
        super();
    }

    public InjectException(String message, Throwable cause) {
        super(message, cause);
    }

    public InjectException(String message) {
        super(message);
    }

    public InjectException(Throwable cause) {
        super(cause);
    }

}
