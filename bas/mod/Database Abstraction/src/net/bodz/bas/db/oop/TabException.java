package net.bodz.bas.db.oop;

public class TabException extends Exception {

    private static final long serialVersionUID = -6958980130316666774L;

    public TabException() {
        super();
    }

    public TabException(String message, Throwable cause) {
        super(message, cause);
    }

    public TabException(String message) {
        super(message);
    }

    public TabException(Throwable cause) {
        super(cause);
    }

}
