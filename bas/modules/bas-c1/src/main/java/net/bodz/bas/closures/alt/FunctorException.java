package net.bodz.bas.closures.alt;

public class FunctorException extends Exception {

    private static final long serialVersionUID = 6370154848236438050L;

    public FunctorException() {
        super();
    }

    public FunctorException(String message, Throwable cause) {
        super(message, cause);
    }

    public FunctorException(String message) {
        super(message);
    }

    public FunctorException(Throwable cause) {
        super(cause);
    }

}
