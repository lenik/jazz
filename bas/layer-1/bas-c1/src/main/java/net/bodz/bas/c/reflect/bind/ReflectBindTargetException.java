package net.bodz.bas.c.reflect.bind;

public class ReflectBindTargetException
        extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public ReflectBindTargetException() {
        super();
    }

    public ReflectBindTargetException(String message, Throwable cause) {
        super(message, cause);
    }

    public ReflectBindTargetException(String message) {
        super(message);
    }

    public ReflectBindTargetException(Throwable cause) {
        super(cause);
    }

}
