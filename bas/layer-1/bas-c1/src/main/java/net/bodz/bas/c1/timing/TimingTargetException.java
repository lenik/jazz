package net.bodz.bas.c1.timing;

public class TimingTargetException
        extends Exception {

    private static final long serialVersionUID = 1L;

    public TimingTargetException() {
        super();
    }

    public TimingTargetException(String message, Throwable cause) {
        super(message, cause);
    }

    public TimingTargetException(String message) {
        super(message);
    }

    public TimingTargetException(Throwable cause) {
        super(cause);
    }

}
