package net.bodz.bas.sugar;

public class ToolsInstantiationException
        extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public ToolsInstantiationException() {
        super();
    }

    public ToolsInstantiationException(String message, Throwable cause) {
        super(message, cause);
    }

    public ToolsInstantiationException(String message) {
        super(message);
    }

    public ToolsInstantiationException(Throwable cause) {
        super(cause);
    }

}
