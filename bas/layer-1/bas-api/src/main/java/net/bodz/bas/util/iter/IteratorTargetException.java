package net.bodz.bas.util.iter;

public class IteratorTargetException
        extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public IteratorTargetException() {
        super();
    }

    public IteratorTargetException(String message, Throwable cause) {
        super(message, cause);
    }

    public IteratorTargetException(String message) {
        super(message);
    }

    public IteratorTargetException(Throwable cause) {
        super(cause);
    }

}
