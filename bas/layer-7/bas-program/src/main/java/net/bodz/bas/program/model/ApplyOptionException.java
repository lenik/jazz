package net.bodz.bas.program.model;

public class ApplyOptionException
        extends Exception {

    private static final long serialVersionUID = 1L;

    public ApplyOptionException() {
        super();
    }

    public ApplyOptionException(String message, Throwable cause) {
        super(message, cause);
    }

    public ApplyOptionException(String message) {
        super(message);
    }

    public ApplyOptionException(Throwable cause) {
        super(cause);
    }

}
