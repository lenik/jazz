package net.bodz.bas.repr.path;

public class PathDispatchException
        extends Exception {

    private static final long serialVersionUID = 1L;

    public PathDispatchException() {
        super();
    }

    public PathDispatchException(String message, Throwable cause) {
        super(message, cause);
    }

    public PathDispatchException(String message) {
        super(message);
    }

    public PathDispatchException(Throwable cause) {
        super(cause);
    }

}
