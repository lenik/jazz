package net.bodz.bas.gui.css3.property;

public enum OutlineColorMode {

    /**
     * Specifies the color of the outline. Look at CSS Color Values for a complete list of possible
     * color values.
     */
    color,

    /**
     * Performs a color inversion. This ensures that the outline is visible, regardless of color
     * background. This is default
     */
    invert,

}
