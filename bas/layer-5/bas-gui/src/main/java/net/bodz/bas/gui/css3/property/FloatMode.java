package net.bodz.bas.gui.css3.property;

public enum FloatMode {

    /**
     * The element generates a block box that is floated to the left. Content flows on the right
     * side of the box, starting at the top (subject to the 'clear' property).
     */
    left,

    /**
     * Similar to 'left', except the box is floated to the right, and content flows on the left side
     * of the box, starting at the top.
     */
    right,

    /** The box is not floated. */
    none,

}
