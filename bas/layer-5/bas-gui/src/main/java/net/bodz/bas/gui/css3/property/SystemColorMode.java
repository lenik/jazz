package net.bodz.bas.gui.css3.property;

public enum SystemColorMode {

    /** Active window border. */
    ActiveBorder,

    /** Active window caption. */
    ActiveCaption,

    /** Background color of multiple document interface. */
    AppWorkspace,

    /** Desktop background. */
    Background,

    /** Face color for three-dimensional display elements. */
    ButtonFace,

    /**
     * Highlight color for three-dimensional display elements (for edges facing away from the light
     * source).
     */
    ButtonHighlight,

    /** Shadow color for three-dimensional display elements. */
    ButtonShadow,

    /** Text on push buttons. */
    ButtonText,

    /** Text in caption, size box, and scrollbar arrow box. */
    CaptionText,

    /**
     * Grayed (disabled) text. This color is set to #000 if the current display driver does not
     * support a solid gray color.
     */
    GrayText,

    /** Item(s) selected in a control. */
    Highlight,

    /** Text of item(s) selected in a control. */
    HighlightText,

    /** Inactive window border. */
    InactiveBorder,

    /** Inactive window caption. */
    InactiveCaption,

    /** Color of text in an inactive caption. */
    InactiveCaptionText,

    /** Background color for tooltip controls. */
    InfoBackground,

    /** Text color for tooltip controls. */
    InfoText,

    /** Menu background. */
    Menu,

    /** Text in menus. */
    MenuText,

    /** Scroll bar gray area. */
    Scrollbar,

    /** Dark shadow for three-dimensional display elements. */
    ThreeDDarkShadow,

    /** Face color for three-dimensional display elements. */
    ThreeDFace,

    /** Highlight color for three-dimensional display elements. */
    ThreeDHighlight,

    /** Light color for three-dimensional display elements (for edges facing the light source). */
    ThreeDLightShadow,

    /** Dark shadow for three-dimensional display elements. */
    ThreeDShadow,

    /** Window background. */
    Window,

    /** Window frame. */
    WindowFrame,

    /** Text in windows. */
    WindowText,

}
