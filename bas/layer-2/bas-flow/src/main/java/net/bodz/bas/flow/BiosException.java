package net.bodz.bas.flow;

public class BiosException extends Exception {

    private static final long serialVersionUID = -2537553751509420233L;

    public BiosException() {
        super();
    }

    public BiosException(String message, Throwable cause) {
        super(message, cause);
    }

    public BiosException(String message) {
        super(message);
    }

    public BiosException(Throwable cause) {
        super(cause);
    }

}
